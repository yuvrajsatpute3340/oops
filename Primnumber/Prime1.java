package Primnumber;

public class Prime1 {

	public static void main(String[] args) {
		int num=0;
		boolean flag=true;
		for(int i=2;i<00;i++)
		{
			if (num%i==0)
			{
				flag=false;
			}
		}
		if (flag==false)
		{
			System.out.println("not prime");
		}
		else
		{
			System.out.println("prime");
		}
		
	}

}

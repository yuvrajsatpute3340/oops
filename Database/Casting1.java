package Database;

public class Casting1 {
		public static void main(String[] args) {
			Float d=50.100f;//football
			System.out.println(d);
			char i='@';//cricket ball
			System.out.println(i);
			//narrowing,Explicit type casting
			
			char age=50;//32bit
			System.out.println(age);
			char d1=age;//64bit
			System.out.println(d1);
			//winding casting
		}

	}

	